public class Main {
    public static void main(String[] args) {
      for (int i = 1; i < 11; i ++) {
          System.out.println("Итераация цикла " + i);
      }
      // В обратном порядке
      for (int y = 10; y > 0; y --) {
          System.out.println("Итераация цикла " + y);
      }
    }
}